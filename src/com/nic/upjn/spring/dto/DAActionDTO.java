package com.nic.upjn.spring.dto;

public class DAActionDTO {

	protected String payCategory;
	protected String ruleOrderNo;
	protected String ruleOrderDate;
	protected String ruleEffectiveDate;
	protected String daPercent;
	protected String dueDate;
	protected String gpfDate;
	protected String cashDate;
	public String getPayCategory() {
		return payCategory;
	}
	public void setPayCategory(String payCategory) {
		this.payCategory = payCategory;
	}
	public String getRuleOrderNo() {
		return ruleOrderNo;
	}
	public void setRuleOrderNo(String ruleOrderNo) {
		this.ruleOrderNo = ruleOrderNo;
	}
	public String getRuleOrderDate() {
		return ruleOrderDate;
	}
	public void setRuleOrderDate(String ruleOrderDate) {
		this.ruleOrderDate = ruleOrderDate;
	}
	public String getRuleEffectiveDate() {
		return ruleEffectiveDate;
	}
	public void setRuleEffectiveDate(String ruleEffectiveDate) {
		this.ruleEffectiveDate = ruleEffectiveDate;
	}
	public String getDaPercent() {
		return daPercent;
	}
	public void setDaPercent(String daPercent) {
		this.daPercent = daPercent;
	}
	public String getDueDate() {
		return dueDate;
	}
	public void setDueDate(String dueDate) {
		this.dueDate = dueDate;
	}
	public String getGpfDate() {
		return gpfDate;
	}
	public void setGpfDate(String gpfDate) {
		this.gpfDate = gpfDate;
	}
	public String getCashDate() {
		return cashDate;
	}
	public void setCashDate(String cashDate) {
		this.cashDate = cashDate;
	}
	
	
	
}
