package com.nic.upjn.spring.dto;

public class RetEmpReportDTO {
	
	private String empcode;
	private String firstName;
	private String lastName;
	private String designationid;
	private String relieveafterdate;
	
	public String getEmpcode() {
		return empcode;
	}
	public void setEmpcode(String empcode) {
		this.empcode = empcode;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getDesignationid() {
		return designationid;
	}
	public void setDesignationid(String designationid) {
		this.designationid = designationid;
	}
	public String getRelieveafterdate() {
		return relieveafterdate;
	}
	public void setRelieveafterdate(String relieveafterdate) {
		this.relieveafterdate = relieveafterdate;
	}
	
	
	
	

}
